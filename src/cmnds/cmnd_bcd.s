.pushseg
	.segment "ZEROPAGE"

	.segment "DATA"
		unsigned char bcd[5]
.popseg

;----------------------------------------------------------------------
;			Chaines statiques
;----------------------------------------------------------------------
.pushseg
	.segment "RODATA"

.popseg

;----------------------------------------------------------------------
;			Programme principal
;----------------------------------------------------------------------
.segment "CODE"

;----------------------------------------------------------------------
;
; Entrée:
;
; Sortie:
;
; Variables:
;	Modifiées:
;		-
;	Utilisées:
;		-
; Sous-routines:
;	-
;----------------------------------------------------------------------
.proc cmnd_bcd
		; X = longueur de la chaine
		; value = adresse de la chaine

		lda	#$00
		ldx	#$04
	init:
		sta	bcd,x
		dex
		bpl	init

	loop:
		inx
		lda	value,x
		bne	loop

		dex

		ldy	#$04+1
	loop1:
		dey
		lda	value,x
		and	#$0f
		sta	bcd,y

		dex
		bmi	end

		lda	value,x
		and	#$0f
		asl
		asl
		asl
		asl
		ora	bcd,y
		sta	bcd,y
		dex
		bpl	loop1

	end:
		clc
		rts
.endproc

