;----------------------------------------------------------------------
.proc cmnd_set_status
		cmp	#$00
		beq	status_off

	status_on:
		; 24 lignes
		cputc	$0c
		lda	#$18
		sta	SCRFY
		clc
		rts

	status_off:
		; 27 lignes
		cputc	$0c
		lda	#$1b
		sta	SCRFY
		clc
		rts
.endproc


