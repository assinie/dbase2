.proc fn_upper
		ldx	#$ff
	loop:
		inx
		lda	string,x
		beq	end
		cmp	#'a'
		bcc	loop
		cmp	#'z'+1
		bcs	loop
		and	#$df
		sta	string,x
		bne	loop

	end:
		; Résultat de type 'C'
		lda	#'C'
		sta	param_type

		clc
		rts

;	error:
