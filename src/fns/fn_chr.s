.proc fn_chr
		lda	param_type
		and	#$7f
		cmp	#'N'
		bne	error

		jsr	is_pfac_byte
		bne	error57

		lda	pfac
		sta	string
		lda	#$00
		sta	string+1

		lda	#'C'
		sta	param_type

		clc
		rts

	error57:
		; 57 CHR() : Out of range.
		lda	#57

	error:
		sec
		rts
.endproc

