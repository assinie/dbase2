;----------------------------------------------------------------------
; <ident> | <string>
;
; Entrée:
;	AX: adresse de la ligne
;	Y: offset
;
; Sortie:
;
; Variables:
;	Modifiées:
;		-
;	Utilisées:
;		-
; Sous-routines:
;	-
;----------------------------------------------------------------------
.proc get_expr_date
		jsr	get_expr
		bcs	end

		lda	param_type
		and	#$7f
		cmp	#'D'
		bne	error

		clc
		rts
	error:
		ldy	lex_prev_y
		; 81: Invalid date.
		; 11: Invalid function argument.
		lda	#11
		sec

	end:
		rts
.endproc

