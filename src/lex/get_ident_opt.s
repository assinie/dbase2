.proc get_ident_opt
		sta	lex_ptr
		stx	lex_ptr+1

		lda	(lex_ptr),y
		beq	no_ident

	_string:
		; Délimiteur: caractère non alphanumérique
		clc
		; Conversion minuscules/MAJUSCULES
		clv
		lda	lex_ptr
		jsr	get_ident
	;	lda	#'I'
	;	sta	param_type
		rts

	no_ident:
		lda	#$00
		sta	ident
	;	sta	param_type
	;	tax

		clc
		rts
.endproc

