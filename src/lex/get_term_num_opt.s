;----------------------------------------------------------------------
; <ident> |
;
; Entrée:
;	AX: adresse de la ligne
;	Y: offset
;
; Sortie:
;	Si valeur:
;		cf get_term_num
;
;	Si pas de valeur:
;		A: 0
;		X: 0
;		Y: inchangé
;		C: 0
;		param_type: 0
;		pfac: inchnagé (à mettre à 0?)
;
; Variables:
;	Modifiées:
;		lex_ptr
;		param_type
;	Utilisées:
;		-
; Sous-routines:
;	get_term_num
;----------------------------------------------------------------------
.proc get_term_num_opt
		sta	lex_ptr
		stx	lex_ptr+1

		lda	(lex_ptr),y
		beq	no_value

		lda	lex_ptr
		jmp	get_term_num

	no_value:
		lda	#$00
		sta	param_type
		tax

		clc
		rts
.endproc

