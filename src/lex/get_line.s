; From lex
.importzp lex_ptr
.importzp pfac

;----------------------------------------------------------------------
;				exports
;----------------------------------------------------------------------
.export get_line

;----------------------------------------------------------------------
;                       Segments vides
;----------------------------------------------------------------------
.segment "STARTUP"
.segment "INIT"
.segment "ONCE"

;----------------------------------------------------------------------
;			Defines / Constantes
;----------------------------------------------------------------------

;----------------------------------------------------------------------
;				Page Zéro
;----------------------------------------------------------------------
.pushseg
	.segment "ZEROPAGE"
.popseg

;----------------------------------------------------------------------
;				Variables
;----------------------------------------------------------------------
.pushseg
	.segment "DATA"

.popseg

;----------------------------------------------------------------------
;			Chaines statiques
;----------------------------------------------------------------------
.pushseg
	.segment "RODATA"

.popseg

;----------------------------------------------------------------------
;			Programme principal
;----------------------------------------------------------------------
.segment "CODE"

;----------------------------------------------------------------------
; .* $
;
; Entrée:
;	AX: adresse de la ligne
;	Y: offset
;
; Sortie:
;	pfac: adresse du paramètre
;
; Variables:
;	Modifiées:
;		lex_ptr
;		pfac
;	Utilisées:
;		-
; Sous-routines:
;	-
;----------------------------------------------------------------------
.proc get_line
		sta	lex_ptr
		stx	lex_ptr+1

		; Sauvegarde le début de la chaine dans pfac
		clc
		sta	pfac
		tya
		adc	pfac
		sta	pfac

		txa
		adc	#$00
		sta	pfac+1

		; Maintenant, on va à la fin de la ligne
		dey
	loop:
		iny
		lda	(lex_ptr),y
		bne	loop

		clc
		rts
.endproc

